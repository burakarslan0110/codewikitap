/**
 * Construct the MCP server with all 7 tools wired up.
 *
 * The `instructions:` field is the agent's first-touch guidance — it tells
 * the model when to call which tool. Keep it tight (≤ 240 chars per claim)
 * to stay under Anthropic's 10%-of-context tool-surface threshold.
 */

